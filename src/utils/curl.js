const curl = () => {
  const randomIndex = Math.floor(Math.random() * userAgents.length);
  const randomUserAgent = userAgents[randomIndex];
  const curlStr = `curl -H "Accept-Encoding: identity" -H "Accept-Language: en" -L -A "${randomUserAgent}"`;
  return curlStr;
};

export default curl;
